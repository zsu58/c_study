#include <stdio.h>

void main()
{
  int nData = 10, nNewData = 20;
  int nResult = 10;
  printf("%d\n", nData == nResult);
  printf("%d\n", nData != nResult);
  printf("%d\n", nData > nNewData);
  printf("%d\n", nData < nNewData);
  printf("%d\n", nData >= nResult);
  printf("%d\n", nData <= nResult);
}
